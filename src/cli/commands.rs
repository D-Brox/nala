/// Upgrade packages.
#[derive(Args, Debug)]
#[clap(visible_aliases = ["full-upgrade", "safe-upgrade"])]
pub struct Upgrade {
	/// Prints the URIs in json and does not perform an upgrade.
	#[clap(long, action)]
	pub print_uris: bool,

	/// Exclude packages from upgrade. Accepts glob patterns.
	#[clap(long, value_name = "PKG", action, add = ArgValueCompleter::new(package_completion))]
	pub exclude: Vec<String>,

	/// Perform a Full Upgrade.
	#[clap(long, action)]
	pub full: bool,

	/// Do NOT perform a Full Upgrade.
	#[clap(long, action)]
	pub no_full: bool,

	/// Perform a Safe Upgrade.
	/// Takes precedence over other Upgrade options.
	#[clap(long, action)]
	pub safe: bool,

	#[clap(flatten)]
	pub transaction: TransactionFlags,

	#[clap(flatten)]
	pub recommends: InstallFlags,

	#[clap(flatten)]
	pub auto_remove: AutoRemoveFlags,
}


/// All supported nala subcommands.
#[derive(Subcommand, Debug)]
#[clap(rename_all = "lower")]
pub enum Commands {
	List(List),
	Search(Search),
	Show(Show),
	Policy(Policy),
	Clean(Clean),
	Download(Download),
	History(History),
	Fetch(Fetch),
	Update(Update),
	Upgrade(Upgrade),
	Install(Install),
	Remove(Remove),
	AutoRemove(AutoRemove),
	#[clap(hide = true, disable_help_flag = true)]
	Moo(Moo),
}

/// List all packages or only packages based on the provided name
#[derive(Args, Debug)]
#[allow(clippy::struct_excessive_bools)]
pub struct List {
	/// Package names to search
	#[clap(required = false, add = ArgValueCompleter::new(package_completion))]
	pub pkg_names: Vec<String>,

	/// Print the full description of each package
	#[clap(long, action)]
	pub description: bool,

	/// Print the summary of each package
	#[clap(long, action)]
	pub summary: bool,

	#[clap(flatten)]
	pub info: InfoFlags,

	/// Only include packages that are installed
	#[clap(short, long, action)]
	pub installed: bool,

	/// Only include packages explicitly installed with Nala
	#[clap(short = 'N', long, action)]
	pub nala_installed: bool,

	/// Only include packages that are upgradable
	#[clap(short, long, action)]
	pub upgradable: bool,

	/// Only include virtual packages
	#[clap(short = 'V', long, action)]
	pub r#virtual: bool,

	#[clap(short = 'm', long, action)]
	pub machine: bool,
}

/// Like `List`, but uses regex and searches package descriptions.
#[derive(Args, Debug)]
pub struct Search {
	/// Search only using pkg names and not descriptions.
	#[clap(long, action)]
	pub names_only: bool,

	// Flatten list commands args into search
	#[clap(flatten)]
	pub list_args: List,
}

